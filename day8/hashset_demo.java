package collectionddemo;

import java.util.*;

public class hashset_demo {
public static void main(String[] args) {
	HashSet<String> set = new HashSet<String>();
	set.add("Apple");
	set.add("Mango");
	set.add("Kiwi");
	set.add("Banana");
	set.add(null);
	System.out.println(set);
	
}
}
